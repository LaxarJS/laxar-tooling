/**
 * Commonly used functions.
 * @private
 * @module utils
 */
'use strict';

module.exports = {
   flatten,
   lookup,
   values,
   identity
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

function flatten( arrays ) {
   return [].concat.apply( [], arrays );
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

function lookup( object ) {
   return function( key ) {
      return object[ key ];
   };
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

function values( object ) {
   return Object.keys( object ).map( lookup( object ) );
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

function identity( value ) {
   return value;
}
